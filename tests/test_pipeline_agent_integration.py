def test_iterative_run_does_not_stop_on_score_drop_when_improvement_stop_disabled(
    tmp_path,
    monkeypatch,
):
    settings, session_factory = _session_factory(tmp_path)
    assessment_calls = []
    final_route_calls = []
    _patch_iterative_dependencies(
        monkeypatch,
        assessment_calls,
        final_route_calls,
        top_scores_by_round=[100.0, 90.0, 80.0],
    )

    with session_factory() as db:
        project = Project(
            project_id="PROJ-ITERATIVE-NO-SCORE-STOP",
            name="Iterative no score stop",
            objective="Explore all requested rounds",
        )
        db.add(project)
        db.commit()

        PipelineOrchestrator(settings).run_iterative(
            db,
            project,
            _crem_only_run_plan("disabled", max_rounds=3, top_n=1),
        )

        saved_orchestrator_run = (
            db.query(AgentRun)
            .filter_by(project_id=project.project_id, agent_name="iterative_orchestrator_agent")
            .one()
        )

    numeric_rounds = [
        item
        for item in saved_orchestrator_run.output_json["rounds"]
        if isinstance(item.get("round"), int)
    ]
    assert len(numeric_rounds) == 3
    assert numeric_rounds[1]["score_improvement"] == -10.0
    assert saved_orchestrator_run.output_json["stop_reason"] == "max_rounds_reached"


def test_iterative_run_score_improvement_stop_can_be_enabled(
    tmp_path,
    monkeypatch,
):
    settings, session_factory = _session_factory(tmp_path)
    assessment_calls = []
    final_route_calls = []
    _patch_iterative_dependencies(
        monkeypatch,
        assessment_calls,
        final_route_calls,
        top_scores_by_round=[100.0, 99.0, 98.0],
    )

    with session_factory() as db:
        project = Project(
            project_id="PROJ-ITERATIVE-SCORE-STOP",
            name="Iterative score stop",
            objective="Stop when configured threshold is missed",
        )
        db.add(project)
        db.commit()

        run_plan = _crem_only_run_plan("disabled", max_rounds=3, top_n=1)
